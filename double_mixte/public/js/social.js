function replaceDigitSocial(digit, array, annee, label) {
	var image;
    switch(array[digit]) {
        case 0:
            image = 'zero.svg';
            break;
        case 1:
            image = 'un.svg';
            break;
        case 2:
            image = 'deux.svg';
            break;
        case 3:
            image = 'trois.svg';
            break;
        case 4:
            image = 'quatre.svg';
            break; 
        case 5:
            image = 'cinq.svg';
            break;
        case 6:
            image = 'six.svg';
            break;
        case 7:
            image = 'sept.svg';
            break;
        case 8:
            image = 'huit.svg';
            break;
        case 9:
            image = 'neuf.svg';
            break; 
        default:
        	image = 'blanc.svg';
    }

    $('#'+annee+label+digit).attr('src', 'img/'+image);
    
    
}

function replaceAllDigitsSocial(array, annee, label) {
	var i = 5-array.length;
    for(i; i <= 4; i++) {
        replaceDigitSocial(i, array, annee, label);
    }
}

